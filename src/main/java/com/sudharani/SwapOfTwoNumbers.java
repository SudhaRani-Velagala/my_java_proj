package com.sudharani;

public class SwapOfTwoNumbers {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("before Swap a="+a+ "and b="+b );
        int temp=a;
        a=b;
        b=temp;
        System.out.println("After Swap a="+a+"and b="+b);
    }
}
